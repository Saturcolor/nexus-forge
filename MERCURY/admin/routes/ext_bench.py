def _get_bench_base(bench_id: str) -> str:
    config = get_config()
    meta = _BENCHMARKS.get(bench_id)
    if not meta:
        return ""
    if not config.get(meta["config_enabled"], False):
        return ""
    default = f"http://localhost:{meta['default_port']}"
    return str(config.get(meta["config_url"], default)).rstrip("/")


def _get_bench_dir(bench_id: str) -> Path:
    meta = _BENCHMARKS.get(bench_id)
    if not meta:
        return _ROOT / bench_id
    return _ROOT / meta["dir"]


def _llamacpp_base() -> str:
    config = get_config()
    return str(config.get("llamacpp_url", "http://localhost:4321")).rstrip("/")


