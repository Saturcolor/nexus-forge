# Sync .env — shared logic for all external benchmarks
# ---------------------------------------------------------------------------


async def _sync_env(bench_id: str, selected_models: list[str] | None = None) -> dict:
    meta = _BENCHMARKS.get(bench_id)
    if not meta:
        return {"error": f"Benchmark '{bench_id}' inconnu"}

    daemon_url = _llamacpp_base()
    if not daemon_url:
        return {"error": "llamacpp non configure"}

    if selected_models:
        # Use user-selected models
        ready_models = selected_models
    else:
        # Fallback: fetch all loaded models from daemon
        try:
            async with httpx.AsyncClient(timeout=10.0) as client:
                resp = await client.get(f"{daemon_url}/mgmt/status")
            instances = resp.json()
        except Exception as e:
            return {"error": f"Daemon inaccessible: {e}"}
        ready_models = [i["model_id"] for i in instances if i.get("ready")]

    if not ready_models:
        return {"error": "Aucun modele selectionne"}

    bench_dir = _get_bench_dir(bench_id)
    if not bench_dir.exists():
        return {"error": f"Dossier {bench_dir} introuvable"}

    llm_models = ",".join(f"llamacpp:{m}" for m in ready_models)
    env_content = f"# Auto-generated by Mercury\nLLAMACPP_HOST={daemon_url}\nLLM_MODELS={llm_models}\nMODEL_REQUEST_TIMEOUT_SECONDS=600\n{meta['extra_env']}"

    env_path = bench_dir / ".env"
    env_path.write_text(env_content, encoding="utf-8")
    logger.info("[%s] .env synced: %d models, daemon=%s", bench_id, len(ready_models), daemon_url)

    return {"ok": True, "daemon_url": daemon_url, "models": ready_models, "llm_models": llm_models}


async def _get_status(bench_id: str) -> dict:
    base = _get_bench_base(bench_id)
    service_ok = False
    if base:
        try:
            async with httpx.AsyncClient(timeout=5.0) as client:
                resp = await client.get(base)
            service_ok = resp.status_code == 200
        except Exception:
            pass

    bench_dir = _get_bench_dir(bench_id)
    env_path = bench_dir / ".env"
    current_models: list[str] = []
    current_daemon = ""
    if env_path.exists():
        for line in env_path.read_text(encoding="utf-8").splitlines():
            if line.startswith("LLM_MODELS="):
                raw = line.split("=", 1)[1]
                current_models = [m.replace("llamacpp:", "") for m in raw.split(",") if m.strip()]
            elif line.startswith("LLAMACPP_HOST="):
                current_daemon = line.split("=", 1)[1].strip()

    meta = _BENCHMARKS.get(bench_id, {})
    proc = _bench_processes.get(bench_id)
    managed = proc is not None and proc.returncode is None
    return {
        "bench_id": bench_id,
        "label": meta.get("label", bench_id),
        "enabled": bool(base),
        "service_online": service_ok,
        "service_url": base,
        "env_exists": env_path.exists(),
        "configured_models": current_models,
        "configured_daemon": current_daemon,
        "bench_dir": str(bench_dir),
        "default_port": meta.get("default_port"),
        "managed_pid": proc.pid if managed else None,
    }


